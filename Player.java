/**
 * Enumeration for the possible states of a cell in the Tic-Tac-Toe game.
 * - `Empty` represents a cell that hasn't been played yet.
 * - `Cross` represents a cell marked by Player 1 (X).
 * - `Nought` represents a cell marked by Player 2 (O).
 */
public enum Player {
    Empty, Cross, Nought
}